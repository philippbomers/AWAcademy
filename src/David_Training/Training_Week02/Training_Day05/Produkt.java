package David_Training.Training_Week02.Training_Day05;

public class Produkt {

    private String name;
    private int preis;

    public Produkt (String name, int preis) {
        this.name = name;
        this.preis = preis;
    }

    public String getName() {
        return this.name;
    }

    public int getPreis() {
        return this.preis;
    }

}
