package Pia_Training.Pia_Grouptraining.Day2;

public class Datatypes {
    public static void main(String[] args) {

        //Primitive datatypes with examples
        byte months = 12;
        System.out.println(months);
        short populationSelk = 836;
        System.out.println(populationSelk);
        int populationSpain = 47_000_000;
        System.out.println(populationSpain);
        long populationAsia = 4_700_000_000L;
        System.out.println(populationAsia);
        float piShort = 3.14159265359F;
        System.out.println(piShort);
        double pi = Math.PI;
        System.out.println(pi);
        boolean hungry = true;
        System.out.println(hungry);
        char initialFirstname = 'P';
        System.out.println(initialFirstname);
    }
}
